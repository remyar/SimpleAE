void SERIAL_Init(void)
{
    Serial.begin(115200);
}

void SERIAL_PutString(String str){
    Serial.println(str);
}

void SERIAL_Putc(unsigned char data)
{
    Serial.write(data);
}

unsigned char SERIAL_Getc(void)
{
    if (Serial.available())
    {
        return Serial.read();
    }
}

bool SERIAL_Available(void){
    return Serial.available();
}
